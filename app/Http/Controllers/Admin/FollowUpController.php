     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'comment' => 'required',
        ]);

       
        // dd(auth()->user()->id);


        Followup::create([
            'user_id' => auth()->user()->id,
            'comment' =>$request->comment,
            'case_id' =>$request->case_id,
        ]);

        return back()->with('message','case follow up added successfully');
    }

